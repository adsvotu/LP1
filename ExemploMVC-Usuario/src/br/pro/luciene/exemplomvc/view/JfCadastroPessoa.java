    private boolean validarCampos() throws Exception {
        String msg = "";
        
        if (jTxtSenha.getText().equals("")) {
            msg+="\nO campo nome n√£o pode ser vazio";
            jTxtSenha.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 51)));
        } else if (jTxtSenha.getText().length() > 50) {
            msg+="\nO nome n√£o pode ter mais de 50 caracteres";
        }
        

        //colocar os demais campos
        
        if (msg.length() == 0)
            return true;
        else {
            JOptionPane.showMessageDialog(null, msg);
            return false;
        }
    }

    private void tratarCampos(boolean f) {
        jTxtSenha.setEnabled(f);
        jBtnExcluir.setEnabled(f);
        jBtnSalvar.setEnabled(f);
        jBtnCancelar.setEnabled(f);
    }

