    private void jtPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtPesquisaKeyReleased
        try {
           
            if (jtPesquisa.getText().length() >= 1) {
                String campo = jcbCampo.getSelectedItem().toString();
                String textoPesquisa = jtPesquisa.getText();
                
                listaUsuarios = usuarioCtrl.pesquisar(campo, textoPesquisa);
                modelo.setNumRows(0);
                
/*                
                //uma forma de fazer
                
                //listaUsuarios.stream().forEach((usuario) -> {
                for (Usuario usuario : listaUsuarios) {
                  modelo.addRow(new Object[] {
                      usuario.getId(),
                      usuario.getLogin(),
                      usuario.getSenha()
                  });    
                }
*/
                
                for (int i=0; i < listaUsuarios.size(); i++) {
                    modelo.addRow(new String[] {
                      String.valueOf(listaUsuarios.get(i).getId()),
                      listaUsuarios.get(i).getLogin(),
                      listaUsuarios.get(i).getSenha()
                  });    
                }
                    
                
                
            } else {
                //limpa a tabela
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, "Erro: "+
                    erro.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }        
        
        
        
    }//GEN-LAST:event_jtPesquisaKeyReleased

